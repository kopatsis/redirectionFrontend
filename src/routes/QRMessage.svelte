
<p>
    QR codes are black and white images that contain an arrangement of black squares. These arrangements can be decoded into a text in the same way that a barcode 
    on a product can be decoded into its product number. These are incredibly useful as the camera on any smartphone can scan a QR code and if the encoded text is a URL, 
    it can take you to this URL.
</p>
<p>
    When you generate a URL, you can know click the "QR Code" button and an image will be generated with a QR code for your shortened URL. You can scan it right here in the 
    browser to be taken to your original URL, or you can click the "Download" button to save or print the QR code. This way it can be re-used.
</p>
<p>
    This feature is optional and the code will only be generated it you click the "QR code" button. A QR code can't be pasted into the browser, so if you 
    do need a shortened URL, it is still available for this task. All of the original features are still here, so no need to worry about them 
    going away!
</p>

